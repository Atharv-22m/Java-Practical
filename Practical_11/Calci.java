    private void jButtonEqualsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEqualsActionPerformed
        // TODO add your handling code here:
        lastElement = screen.getText().substring(screen.getText().length()-1).hashCode();
        if(lastElement == 32){
            screen.setText("Error");
        }else{
        input = screen.getText().split(" ");
        values = new ArrayList<>(Arrays.asList(input));
        ans = 0.0;
        
        for (int i = 0; i < values.size(); i++) {
            if (values.get(i).equals("/")) {
                ans = Double.parseDouble(values.get(i-1)) / Double.parseDouble(values.get(i+1));
                values.set(i-1,Double.toString(ans));

                values.remove(i+1);
                values.remove(i);
                i = -1;
            }
        }
        
        for (int i = 0; i < values.size(); i++) {
            if (values.get(i).equals("*")) {
                ans = Double.parseDouble(values.get(i-1)) * Double.parseDouble(values.get(i+1));
                values.set(i-1,Double.toString(ans));

                values.remove(i+1);
                values.remove(i);
                i = -1;
            }
        }
        
        for (int i = 0; i < values.size(); i++) {
            if (values.get(i).equals("-")) {
                ans = Double.parseDouble(values.get(i-1)) - Double.parseDouble(values.get(i+1));
                values.set(i-1,Double.toString(ans));

                values.remove(i+1);
                values.remove(i);
                i = -1;
            }
        }
        
        for (int i = 0; i < values.size(); i++) {
            if (values.get(i).equals("+")) {
                ans = Double.parseDouble(values.get(i-1)) + Double.parseDouble(values.get(i+1));
                values.set(i-1,Double.toString(ans));

                values.remove(i+1);
                values.remove(i);
                i = -1;
            }
        }
        expression.setText(screen.getText());
        screen.setText(values.get(0));
        }
    }//GEN-LAST:event_jButtonEqualsActionPerformed

